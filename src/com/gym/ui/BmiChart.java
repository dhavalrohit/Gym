    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        try{
            String name,gender,height,weight,bmi,fat,category = null,waist,wrist,forearm,hips;
            gender=gen;
            name=jTextField1.getText();
            height=jTextField3.getText();
            weight=jTextField2.getText();
            bmi=jTextField5.getText();
            waist=jTextField6.getText();
            wrist=jTextField8.getText();
            hips=jTextField10.getText();
            forearm=jTextField9.getText();
            fat=jTextField7.getText();
            float f=Float.parseFloat(fat);
            if(gender.equals("male"))
            {
                if(f>=2 && f<=4)
                {
                    category="Essential Fat";
                }
                else if(f>=6 && f<=13)
                {
                    category="Athletic";
                }
                else if(f>=14 && f<=17)
                {
                    category="Fitness";
                }
                else if(f>=18 && f<=25)
                {
                    category="average";
                }
                else if(f>=26)
                {
                    category="Obese";
                }
            }
            else
            {
                if(f>=10 && f<=12)
                {
                    category="Essential Fat";
                }
                else if(f>=14 && f<=20)
                {
                    category="Athletic";
                }
                else if(f>=21 && f<=24)
                {
                    category="Fitness";
                }
                else if(f>=25 && f<=31)
                {
                    category="average";
                }
                else if(f>=32)
                {
                    category="Obese";
                }
            }
            PrintBMIReport pbm=new PrintBMIReport();
            pbm.jLabel12.setText(name);
            pbm.jLabel13.setText(gender);
            pbm.jLabel14.setText(weight);
            pbm.jLabel15.setText(height);
            pbm.jLabel16.setText(bmi);
            pbm.jLabel17.setText(waist);
            pbm.jLabel18.setText(wrist);
            pbm.jLabel19.setText(forearm);
            pbm.jLabel20.setText(hips);
            pbm.jLabel21.setText(fat);
            pbm.jLabel22.setText(category);
            pbm.setVisible(rootPaneCheckingEnabled);
                    
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }                                        

