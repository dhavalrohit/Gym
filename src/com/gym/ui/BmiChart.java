    public void calculateFatForMan()
    {
        kg=Float.parseFloat(jTextField2.getText());
        pound=kg*2.2046f;
        float waist=Float.parseFloat(jTextField6.getText());
        float val1=(pound*1.082f)+94.42f;
        float val2=(waist*4.15f);
        float lbm=val1-val2;
        float val3=pound-lbm;
        float bmass=val3/pound*100;
        jTextField7.setText(String.valueOf(bmass));
    }
    public void calculateFatForWoman()
    {
        kg=Float.parseFloat(jTextField2.getText());
        float waist=Float.parseFloat(jTextField6.getText());
        float hips=Float.parseFloat(jTextField10.getText());
        float forearm=Float.parseFloat(jTextField9.getText());
        pound=kg*2.2046f;
        value1=pound*0.732f;
        value2=value1+8.987f;
        value3=waist/3.14f;
        value4=waist*0.157f;
        value5=hips*0.249f;
        value6=forearm*0.434f;
        value7=value2+value3;
        value8=value7-value4;
        value9=value8-value5;
        float lbm=value6+value9;
        value10=pound-lbm;
        float fat=value10/pound*100;
        jTextField7.setText(String.valueOf(fat));
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
       if(gen.equalsIgnoreCase("male"))
           calculateFatForMan();
       else
           calculateFatForWoman();
    }                                        
    
